#Текстовый файл состоит из символов I, U, V, W, X, U и Z.
#Определите в прилагаемом файле максимальное количество идущих подряд символов (длину непрерывной подпоследовательности),
#среди которых символ Х встречается не более 140 раз.

def find_max_continuous_subsequence(file_path):
    # Открываем файл для чтения
    with open(file_path, 'r') as file:
        # Читаем содержимое файла в переменную text
        text = file.read()

    # Инициализируем переменные для отслеживания максимальной длины и текущей длины подпоследовательности
    max_length = 0
    current_length = 0
    x_count = 0

    # Проходим по каждому символу в тексте
    for char in text:
        # Если символ является одной из букв ['I', 'U', 'V', 'W', 'X', 'U', 'Z']
        if char in ['I', 'U', 'V', 'W', 'X', 'U', 'Z']:
            # Увеличиваем текущую длину подпоследовательности
            current_length += 1
            # Если символ равен 'X', увеличиваем счетчик 'X'
            if char == 'X':
                x_count += 1
            # Если счетчик 'X' превышает 140, сбрасываем текущую длину и счетчик 'X'
            if x_count > 140:
                current_length = 0
                x_count = 0
        # Если символ не является одной из букв, сбрасываем текущую длину и счетчик 'X'
        else:
            current_length = 0
            x_count = 0
        # Обновляем максимальную длину, если текущая длина больше
        max_length = max(max_length, current_length)

    # Возвращаем максимальную длину непрерывной подпоследовательности
    return max_length

# Пример использования
file_path = '313_24.txt'
max_length = find_max_continuous_subsequence(file_path)
print(f"Максимальная длина непрерывной подпоследовательности: {max_length}")